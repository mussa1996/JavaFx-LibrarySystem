  public void populateTable(){
        List<Checkout> stu = new ArrayList<>();
        stu = bo.listcheckout();
       for (Checkout checkout : stu) {
          model.insertRow(model.getRowCount(), new Object[]{
                checkout.getSearchclient(),
              checkout.getClientname(),
              checkout.getPicture(),
              checkout.getSearchbook(),
              checkout.getBooktitle(),
              checkout.getAuthor(),
              checkout.getStatus(),
              checkout.getDate()
            });      
       }
    }
