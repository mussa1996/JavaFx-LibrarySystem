    private void btnSavecheckinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSavecheckinActionPerformed
        // TODO add your handling code here:
        String boid=txtidbI.getText();
        String botitle=txttitleI.getText();
        String auth=txtauthorI.getText();
        String stat=txtstatus.getText();
        String dat=datein.getText();
        SimpleDateFormat date=new SimpleDateFormat("yyyy-mm-dd");
        Checkin che=new Checkin();
        che.setSearchbook(boid);
        che.setBooktitle(botitle);
        che.setAuthor(auth);
        che.setStatus(stat);
        try {
            che.setDate(date.parse(dat));
        } catch (ParseException ex) {
            Logger.getLogger(Checks.class.getName()).log(Level.SEVERE, null, ex);
        }
        bo.addCheckin(che);
        mode.setRowCount(0);
        populateTable1();
    }//GEN-LAST:event_btnSavecheckinActionPerformed

