     public void populateTable1(){
     
        List<Bookcategory> departments = new ArrayList<>();
        departments=bo.listCategory();
         for (Bookcategory department: departments) {
          model.insertRow(model.getRowCount(), new Object[]{
                department.getCategoryid(),
                department.getCategoryname()});
            
         }
            
     }
     public void populateTable(){
        
        List<Book> students = new ArrayList<>();
        students = bo.listBook();
        for (Book student : students) {
            mode.insertRow(mode.getRowCount(), new Object[]{
                student.getBookid(),
                student.getTitle(),
                student.getPublishinghouse(),
                student.getDateofpublication(),
                student.getAuthor(),
                student.getPages(),
                student.getBookcategory().getCategoryid()
            });
        }
    }
