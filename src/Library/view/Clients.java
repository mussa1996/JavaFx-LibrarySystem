    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile(); 
         //photo.setIcon(new ImageIcon(f.toString()));
        filename=f.getAbsolutePath();
        txtimage.setText(filename);
        Image im = Toolkit.getDefaultToolkit().createImage(filename);
        im =im.getScaledInstance(labelimage.getWidth(), labelimage.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon ic = new ImageIcon(im);
        labelimage.setIcon(ic);
    }//GEN-LAST:event_jButton2ActionPerformed

